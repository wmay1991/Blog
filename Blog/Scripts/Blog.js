//JS file used for any custom DOM manipulation for the 

$(document).on('click', "#searchButton", function () {
            var searchTerm = $("#searchTerm").val();
            SearchCall(searchTerm);
 });

  
function SearchCall(searchTerm){
    $.ajax({
        method: "GET",
        url: "/Home/Search",
        data: {searchTerm : searchTerm}
    }
   )
};
